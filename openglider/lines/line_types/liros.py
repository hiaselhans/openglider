def TSL(strength: int, diameter: float, stretch_curve: list[list[float]], weight: float) -> None:
    LineType(
        f"liros.tsl{strength:03d}",
        diameter,
        stretch_curve,
        10*strength,
        weight,
        sheated=True,
        colors=["blue", "green", "red", "yellow", "orange"]
        )

